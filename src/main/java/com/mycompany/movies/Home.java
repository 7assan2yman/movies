    private void setupMovieTable() {
        // Create dynamic table model with 0 initial rows
        String[] columns = {"Title", "Year", "Rating", "Votes", "Directors", "Stars"};
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(columns, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Make table read-only
            }
        };

        // Set the dynamic model to your table
        jTable1.setModel(model);

        // Set proper column widths
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(200); // Title
        jTable1.getColumnModel().getColumn(1).setPreferredWidth(60);  // Year
        jTable1.getColumnModel().getColumn(2).setPreferredWidth(80);  // Rating
        jTable1.getColumnModel().getColumn(3).setPreferredWidth(100); // Votes
        jTable1.getColumnModel().getColumn(4).setPreferredWidth(150); // Directors
        jTable1.getColumnModel().getColumn(5).setPreferredWidth(200); // Stars

        // Enable sorting - users can click column headers to sort
        jTable1.setAutoCreateRowSorter(true);
    }

private void displayMovies(java.util.List movies) {
    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // Clear existing data
    
    for (int i = 0; i < movies.size(); i++) {
        Movie movie = (Movie) movies.get(i); // Cast to Movie
        model.addRow(new Object[]{
            movie.getTitle(),
            movie.getYear(),
            movie.getRating(),
            movie.getVotes(),
            movie.getDirectors(),
            movie.getStars()
        });
    }
    
    // Show result count in window title
}
