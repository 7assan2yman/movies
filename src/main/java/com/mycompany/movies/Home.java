    private void btn_star_insightsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_star_insightsActionPerformed
        // TODO add your handling code here:
        String searchBy = (String) cbx_search_by.getSelectedItem();
        String searchTerm = txt_field_search.getText().trim();

        // Check if user is searching by Stars
        if (searchBy.equals("Stars") && !searchTerm.isEmpty()) {
            try {
                // Open StarInsights with the searched actor name
                StarInsights starFrame = new StarInsights(searchTerm);
                starFrame.setVisible(true);

            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, 
                    "Error opening Star Insights: " + e.getMessage(), "Error", 
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        } else {
            // Not searching by stars or empty search - ask for actor name
            javax.swing.JOptionPane.showMessageDialog(this, 
                "To see Star Insights:\n\n" +
                "1. Select 'Stars' from the Search By dropdown\n" +
                "2. Enter an actor name in the search box\n" +
                "3. Click Star Insights button\n\n" +
                "Or you can search for movies by actor first.", 
                "How to Use Star Insights", 
                javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
    
    }//GEN-LAST:event_btn_star_insightsActionPerformed

