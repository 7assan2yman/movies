    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
        // TODO add your handling code here:
        try {
            String searchTerm = txt_field_search.getText().trim();
            String searchBy = (String) cbx_search_by.getSelectedItem();
            
            java.util.List movies;
            
            if (searchTerm.isEmpty()) {
                // If search is empty, show all movies
                movies = db.getAllMovies();
            } else {
                // Search with term
                movies = db.searchMovies(searchTerm, searchBy);
            }
            
            displayMovies(movies);
            
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Search error: " + ex.getMessage(), "Error", 
                javax.swing.JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btn_searchActionPerformed

